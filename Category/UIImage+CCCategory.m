/**
 *  计算图片缩放的比例
 *
 *  @param originalSize 图片原始大小
 *  @param targetSize   图片目的大小
 *  @return
 */
static inline CGFloat calculateInSampleSize(CGSize originalSize, CGSize targetSize)
{
    CGFloat oHeight = originalSize.height;
    CGFloat oWidth = originalSize.width;
    
    CGFloat tHeight = targetSize.height;
    CGFloat tWidth = targetSize.width;
    CGFloat inSampleSize = 1;
    if (tHeight == 0 || tWidth == 0) return 1;
    if (oHeight < tHeight && oWidth < tWidth) return 1;
    
    CGFloat heightRatio = tHeight / oHeight;
    CGFloat widthRatio = tWidth / oWidth;
    inSampleSize = heightRatio > widthRatio ? heightRatio : widthRatio;
    return inSampleSize;
}

@implementation UIImage (CCCategory)

- (UIImage *)scaleToSize:(CGSize)targetSize
{
    return [UIImage scaleImage:self toSize:targetSize];
}

+ (UIImage *)scaleImage:(UIImage *)image toSize:(CGSize)targetSize
{
    CGFloat inSampleSize = calculateInSampleSize(image.size, targetSize);
    NSLog(@"fuck --scale-- %f  %f  %f", image.size.width, image.size.height, inSampleSize);
    if (inSampleSize == 1) {
        return image;
    }
    
    CGFloat outWidth = image.size.width / inSampleSize;
    CGFloat outHeight = image.size.height / inSampleSize;
    NSLog(@"fuck---scaled-- %f  %f", outWidth, outHeight);
    UIGraphicsBeginImageContextWithOptions(targetSize, NO, 0.0f);
    CGRect imageRect = CGRectMake(0.0, 0.0, outWidth, outHeight);
    [image drawInRect:imageRect];
    UIImage *scaleImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return scaleImage;
}

